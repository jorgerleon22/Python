#suma y resta (+ y -)
suma = 12 + 5
resta = 12 -5 

#multi y división (* y /)
multi = 12 * 5
division = 12 / 5 #devuelve un dato float (flotante)

#potencias (exponente) (**)
exponente = 12**5

#division baja (//)
division_baja = 12 // 5 #devuelve entero redondeado hacia abajo

#resto o módulo
resto = 12%5

print(suma,resta,multi,division,exponente,division_baja,resto)

#type(dato) nos devuelve que tipo de dato es
tipo_de_dato = type(5) 
print(tipo_de_dato)

tipo_de_dato = type(division)
print(tipo_de_dato)